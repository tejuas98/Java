package Basic_chezze;

public class CharDataTypes {

    public static void main(String[] args) {

        char rudra = 'r';
        System.out.println(rudra);

        char adi = 'R';
        System.out.println(adi);

        char adi2 = '%';
        System.out.println(adi2);

        char recf = '6';
        System.out.println(recf);
    }
}



