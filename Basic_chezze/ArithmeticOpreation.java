package Basic_chezze;

public class ArithmeticOpreation {
    public static void main(String[] args) {
// varabiles = int(integer), double(Decimal)
        int x = 20, y = 10 ;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);


        // int a = 4;
        // int b = 9;   it can also used like this

        double a = 20 , b = 10 ;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

    }
}


